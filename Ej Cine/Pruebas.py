# Suma
print("Suma con enteros y cadenas:")
print("5 + 3 =", 5 + 3)  # Suma de enteros
print("'Hola ' + 'Mundo' =", 'Hola ' + 'Mundo')  # Concatenación de cadenas

# Resta
print("\nResta:")
print("10 - 3 =", 10 - 3)  # Resta de enteros
# print("'Hola' - 'Mundo'")  # Esto da error: no se puede restar cadenas

# Multiplicación
print("\nMultiplicación con enteros y cadenas:")
print("4 * 3 =", 4 * 3)  # Multiplicación de enteros
print("'Hola' * 3 =", 'Hola' * 3)  # Repetición de cadenas

# División
print("\nDivisión:")
print("8 / 2 =", 8 / 2)  # División de enteros
# print("'Hola' / 2")  # Esto da error: no se pueden dividir cadenas

# División entera
print("\nDivisión entera:")
print("8 // 3 =", 8 // 3)  # División entera entre enteros
# print("'Hola' // 2")  # Esto da error: no se pueden dividir cadenas enteramente

# Módulo
print("\nMódulo (resto de la división):")
print("8 % 3 =", 8 % 3)  # Módulo entre enteros
# print("'Hola' % 3")  # Esto da error: no se pueden calcular módulos de cadenas

# Exponenciación
print("\nExponenciación:")
print("2 ** 3 =", 2 ** 3)  # Exponenciación entre enteros
# print("'Hola' ** 3")  # Esto da error: no se puede elevar una cadena

# Operaciones mixtas
print("\nOperaciones mixtas:")
print("int('5') + 3 =", int('5') + 3)  # Convertir cadena a entero y sumar
print("str(5) + '3' =", str(5) + '3')  # Convertir entero a cadena y concatenar
print("float(5) * 2 =", float(5) * 2)  # Convertir entero a flotante y multiplicar
print("len('Hola') * 2 =", len('Hola') * 2)  # Longitud de una cadena multiplicada
